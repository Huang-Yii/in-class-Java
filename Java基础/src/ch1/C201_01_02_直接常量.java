package ch1;

public class C201_01_02_直接常量 {
    public static void main(String[] args) {
        System.out.println(34);
        System.out.println(3.1415926);
        System.out.println("九江学院");
        System.out.println(034);
        System.out.println(0x34);
        System.out.println(1.6e8);

        float f = 3.14f;
    }
}
